package com.jeremy.Customer.calendar;

public class Day {
	private int id;
	private int year;
	private int month;
	private String day;
	
	public void setDay(String day) {
		this.day = day;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	
	
	

}
